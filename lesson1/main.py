# типы данных

# str -> string (строка)
a = "home"
# int -> integer (целое число)
b = 9
# float -> float (дробное число)
c = 9.9
# bool -> boolean (истенность выражения)
d = False
# list -> list (список можно изменить)
e = ["home2", 8, a, b]
# tuple -> tuple (список нельзя изменить)
f = ("home2", 8, a, b)
# dict
g = {
    "a": 6,
    8: "hi"
}
